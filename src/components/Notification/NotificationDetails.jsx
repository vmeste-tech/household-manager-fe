const NotificationDetails = ({ notification, onBack }) => {
  if (!notification) {
    return (
      <div className="h-full flex items-center justify-center p-6 text-gray-500">
        Выберите уведомление для просмотра подробностей
      </div>
    );
  }

  const date = new Date(notification.datetime);
  const formattedDatetime = date.toLocaleString("ru-RU", {
    day: "2-digit",
    month: "long",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });

  const icon = iconMapping[notification.category.toLowerCase()] || defaultIcon;

  return (
    <div className="p-6 h-full flex flex-col">
      {onBack && (
        <button
          onClick={onBack}
          className="mb-4 flex items-center text-indigo-600 hover:text-indigo-800 transition-colors duration-200 md:hidden"
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            className="h-5 w-5 mr-1"
            fill="none"
            viewBox="0 0 24 24"
            stroke="currentColor"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M15 19l-7-7 7-7"
            />
          </svg>
          Назад
        </button>
      )}

      <div className="border-b border-gray-100 pb-4 mb-4">
        <div className="flex items-center">
          <div className="h-12 w-12 rounded-full bg-indigo-100 flex items-center justify-center text-indigo-600 mr-4">
            {icon}
          </div>
          <div>
            <h2 className="text-xl font-bold text-gray-800">
              {notification.category}
            </h2>
            <time
              dateTime={notification.datetime}
              className="text-sm text-gray-500"
            >
              {formattedDatetime}
            </time>
          </div>
        </div>
      </div>

      <div className="flex-grow mb-4">
        <p className="text-gray-700 whitespace-pre-line">
          {notification.description}
        </p>
      </div>

      <div className="text-right text-xs text-gray-500">
        ID: {notification.id}
      </div>
    </div>
  );
};

NotificationDetails.propTypes = {
  notification: PropTypes.shape({
    id: PropTypes.number.isRequired,
    category: PropTypes.string.isRequired,
    description: PropTypes.string.isRequired,
    datetime: PropTypes.string.isRequired,
  }),
  onBack: PropTypes.func,
};

export default NotificationDetails;
